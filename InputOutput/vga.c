#include "cmd.h"
void vga_init()
{
	keyx = 0;
	keyy = 0;
	vga_set(vga_registers);
    vga_cls(4);
    cmd_mode("VGA Mode");
    term_ptr(vga_printc, vga_prints, vga_setfgbg, vga_setpos);
    cmd_getposptr(vga_getr, vga_getc);
}