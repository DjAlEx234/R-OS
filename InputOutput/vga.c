void vga_set(unsigned char *registers)
{
    int i;
    outb(0x3C2, *registers);
    registers++;
    for (i = 0; i < 5; i++)
    {
        outb(0x3C4, i);
        outb(0x3C5, *registers);
        registers++;
    }
    outb(0x3D4, 0x03);
    outb(0x3D5, inb(0x3D5) | 0x80);
    outb(0x3D4, 0x11);
    outb(0x3D5, inb(0x3D5) & ~0x80);
    registers[0x03] |= 0x80;
	registers[0x11] &= ~0x80;
    for(i = 0; i < 25; i++)
	{
		outb(0x3D4, i);
		outb(0x3D5, *registers);
		registers++;
	}
	for(i = 0; i < 9; i++)
	{
		outb(0x3CE, i);
		outb(0x3CF, *registers);
		registers++;
	}
	for(i = 0; i < 21; i++)
	{
		(void)inb(0x3DA);
		outb(0x3C0, i);
		outb(0x3C0, *registers);
		registers++;
	}
	(void)inb(0x3DA);
	outb(0x3C0, 0x20);
}
void vga_setp(int x, int y, int c)
{
    unsigned char* where = (unsigned char*)0xA0000 + 320 * y + x;
    *where = c;
}
int color, color2;
void vga_cls(int c)
{
    for (int x = 0; x < 320; x++)
        for (int y = 0; y < 200; y++)
            vga_setp(x, y, c);
    color2 = c;
}
void vga_print(unsigned char c, int x, int y, int co, int co2)
{
    int cx, cy;
	int mask[8] = {128, 64, 32, 16, 8, 4, 2, 1};
    unsigned char *font = vga_font + (int)c * 8;
    for (cy = 0; cy < 8; cy++)
    {
        for (cx = 0; cx < 8; cx++)
        {
            if (font[cy] & mask[cx])
                vga_setp(x + cx, y + cy, co);
            else
                vga_setp(x + cx, y + cy, co2);
        }
    }
}
#include "keybd.h"
int keyx, keyy;
void vga_scroll()
{
	vga_cls(color2);
    keyx = 0;
	keyy = 0;
}
void vga_rectfill(int x1, int y1, int x2, int y2, int c)
{
	for (int i = x1; i < x2; i++)
		for (int j = y1; j < y2; j++)
			vga_setp(i, j, c);
}
#include "serial.h"
void vga_printc(int key)
{
    if (key == '\0')
		return;
	if (key == '\b')
	{
		if (keyx == 0)
		{
			keyx = 320;
			keyy = keyy - 8;
		}
		keyx = keyx - 8;
		vga_rectfill(keyx, keyy, keyx + 8, keyy + 8, color2);
		return;
	}
	else if (key == '\n')
	{
		keyy = keyy + 8;
		if (keyy == 200)
			vga_scroll();
		keyx = 0;
		return;
	}
	if (keyy == 200)
	{
		vga_scroll();
		return;
	}
	vga_print(key, keyx, keyy, color, color2);
	keyx = keyx + 8;
	if (keyx == 320)
	{
		keyx = 0;
		keyy = keyy + 8;
	}
}
#include "string.h"
void vga_prints(char* str)
{
	for (int i = 0; i < string_len(str); i++)
        vga_printc(str[i]);
}
void vga_setfgbg(int fg, int bg)
{
    color = fg;
    color2 = bg;
}
void vga_setpos(int r, int c)
{
    keyy = r * 8;
    keyx = c * 8;
}
int vga_getr()
{
    return keyy / 8;
}
int vga_getc()
{
    return keyx / 8;
}
